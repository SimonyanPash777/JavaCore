public class Calculator1 {
    public double plus(double one, double two){
        return one + two;
    }
    public double minus(double three, double four){
        return three - four;
    }
    public double multiply(double five, double six){
        return five * six;
    }
    public double divide(double seven, double eight){
        return seven % eight;
    }
}
