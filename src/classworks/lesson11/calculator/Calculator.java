package classworks.lesson11.calculator;

public class Calculator {
    double plus(double b, double e) {
        return b + e;
    }

    double minus(double c, double v) {
        return c - v;
    }

    double multiply(double h, double w) {
        return h * w;
    }

    double divide(double a, double s) {
        return a / s;
    }
}
