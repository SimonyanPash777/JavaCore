package homework.calculator1;

public class Calculator1 {
    double plus (double one, double two){
        return one + two;
    }
    double minus (double three, double four){
        return three - four;
    }
    double multiply (double five, double six){
        return five * six;
    }
    double divide (double seven, double eight){
        return seven % eight;
    }
}
